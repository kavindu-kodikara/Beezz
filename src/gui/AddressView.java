    private void loadDistrict() {

        try {

            ResultSet resultSet = MySQL.execute("SELECT * FROM `district` ORDER BY `name` ASC");
            Vector<String> vector = new Vector<>();
            vector.add("Select");
            while (resultSet.next()) {
                vector.add(resultSet.getString("name"));
                districtMap.put(resultSet.getString("name"), resultSet.getString("id"));
            }
            DefaultComboBoxModel model = new DefaultComboBoxModel(vector);
            jComboBox1.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void loadName() {
        try {

            ResultSet resultSet = MySQL.execute("SELECT * FROM `customers` WHERE `mobile` = '" + this.mobile + "'");
            resultSet.next();
            jLabel1.setText(resultSet.getString("fname") + " " + resultSet.getString("lname"));

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void loadAddress() {

        try {

            ResultSet resultSet = MySQL.execute("SELECT * FROM `customer_address`"
                    + "INNER JOIN `district` ON `district`.`id` = `customer_address`.`district_id`"
                    + " WHERE `customers_mobile` = '" + this.mobile + "'");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            while (resultSet.next()) {
                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("customer_address_id"));
                vector.add(resultSet.getString("line1"));
                vector.add(resultSet.getString("line2"));
                vector.add(resultSet.getString("name"));

                model.addRow(vector);
            }
            jTable1.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

