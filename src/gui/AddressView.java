    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String line1 = jTextField3.getText();
        String line2 = jTextField4.getText();
        String district = String.valueOf(jComboBox1.getSelectedItem());

        if (line1.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pleace enter Address line 1", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (line2.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Pleace enter Address line 2", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (district.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Pleace select Address District", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {

                MySQL.execute("INSERT INTO `customer_address` "
                        + "(`line1`,`line2`,`district_id`,`customers_mobile`) "
                        + "VALUES ('" + line1 + "','" + line2 + "','" + districtMap.get(district) + "','" + this.mobile + "')");

                reset();
                loadAddress();

            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        if (this.updateAddress) {
            int row = jTable1.getSelectedRow();

            String line1 = jTextField3.getText();
            String line2 = jTextField4.getText();
            String district = String.valueOf(jComboBox1.getSelectedItem());

            if (line1.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Pleace enter Address line 1", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (line2.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Pleace enter Address line 2", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (district.equals("Select")) {
                JOptionPane.showMessageDialog(this, "Pleace select Address District", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {

                try {

                    MySQL.execute("UPDATE `customer_address` SET `line1` = '" + line1 + "',`line2` = '" + line2 + "',"
                            + "`district_id` = '" + districtMap.get(district) + "' WHERE `customer_address_id` = '" + String.valueOf(jTable1.getValueAt(row, 0)) + "'");

                    JOptionPane.showMessageDialog(this, "Update successfull", "Done", JOptionPane.PLAIN_MESSAGE);
                    reset();
                    loadAddress();

                } catch (Exception e) {
                    e.printStackTrace();
                }

            }
        }


    }//GEN-LAST:event_jButton2ActionPerformed

